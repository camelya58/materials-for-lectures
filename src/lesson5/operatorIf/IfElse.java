package lesson5.operatorIf;

public class IfElse {
    public static void main(String[] args) {
        // Case 2: if - else
        int x = 10;
        if (x > 20) {
            System.out.println("Это оператор if");
        } else {
            System.out.println("Это оператор else");
        }
    }
}
